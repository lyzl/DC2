public class DC2PowerManagementPolicy {
    public Integer optimalCpuNum;
    public void checkOptimalCPUnumber(int optimalCpuNum) {
        if (optimalCpuNum < 0) {
            System.out.println("False Value");

        } else {
            System.out.println("True Value");
        }
    }
    public void checkNegativeCPUnumber(int optimalCpuNum) {
        if (optimalCpuNum < 0) {
            System.out.println("False Value");

        } else {
            System.out.println("True Value");
        }
    }
}
